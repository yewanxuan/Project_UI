package com.ywx.bean;

public class TypeSingle {
	private int id;
	private int type_sign;
	private String type_name;
	private int type_pid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType_sign() {
		return type_sign;
	}
	public void setType_sign(int type_sign) {
		this.type_sign = type_sign;
	}
	public String getType_name() {
		return type_name;
	}
	public void setType_name(String type_name) {
		this.type_name = type_name;
	}
	public int getType_pid() {
		return type_pid;
	}
	public void setType_pid(int type_pid) {
		this.type_pid = type_pid;
	}

	
}
